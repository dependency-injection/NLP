{
 rlen<-strtoi(leng)
 python.assign("wlen",rlen)
 python.exec("fdist=FreqDist([word.lower() for word in set(text1) if word.isalpha() and len(word) > wlen])")
 python.exec("dss=fdist.keys()") 
 rkeys<-python.get("dss")
 vec<-c()
 for (i in 1:length(rkeys))
 {
	flag<-i - 1
	python.assign("wo",flag)
	python.exec("d=fdist[dss[wo]]")
	woc<-python.get("d")
	vec<-c(vec,woc)
 }
 print(rkeys)
 print(vec)
 whatever<-data.frame(rkeys,vec)
 qplot(rkeys,weight=vec,data=whatever,geom="bar", ylab=" # whatever")
 }
